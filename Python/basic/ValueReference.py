x = [4, 2, 3, 1]
y = sorted(x, reverse=True)
print(x)
print("the list x is", x, "also the list y is ", y)

# tuple
z = (4, 2, 3, 1)
a = sorted(z)
print(z)
print(a)

# dict keys
b = {"name": "chad", "age": 25}
c = sorted(b)
print(b)
print(c)

# set
x = {4, 5, 3, 2, 1}
y = sorted(x)
print(x)
print(y)

# string
x = "How are you doing today?"
y = sorted(x)
print(x)
print(y)
