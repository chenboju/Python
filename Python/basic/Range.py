# range(start,stop,step)
print(range(100))

for i in range(5):  # 0~4
    print(i)
print("\n")

for i in range(10, 15):
    print(i)
print("\n")

for i in range(0, 100, 10):
    print(i)
print("\n")

for i in range(100, 0, -10):
    print(i)
print("\n")

myList = list(range(0, 15, 2))
print(myList)
